public class WhileLoop {
    public static void main(String[] args) {
        /*
         * 
         * While loop is used to execute a block of code repeatedly as long as the
         * specified condition is true
         * 
         * while(expression){
         * some codes here;
         * }
         * 
         */

        int i = 0;
        while (i < 11) {
            System.out.print(" --> " + i);
            i++;
        }

        /*
         * 
         * while loop is used when the number of iteration is not known beforehand and
         * depends on a condition
         * 
         * It checks the condiiton before each iteration, and if the condition is false
         * initially, the loop may not execute at all.
         * 
         */

    }
}
